package Astro::SIMBAD;

=head1 NAME

Astro::SIMBAD - An object orientated interface to CDS' SIMBAD database

=head1 DESCRIPTION

This module does nothing, and is here as a placeholder in case of need. The
user interface to the goodness wrapped in the SIMBAD package can be found in
the L<Astro::SIMBAD::Query> module.

=head1 REVISION

$Id: SIMBAD.pm,v 1.1 2003/07/15 20:35:57 aa Exp $

=head1 METHODS

The Astro::SIMBAD module itself has no methods, and is just a placeholder
module, see L<Astro::SIMBAD::Query> for the actual interface.

